    private static void bubbleSort1(int[] a) {
        int i, j;
        //表示 n 次排序过程.
        for (i = 0; i < a.length; i++) {
            for (j = 1; j < a.length - i; j++) {
                //前面的数字大于后面的数字就交换 -> 交换a[j-1]和a[j]
                if (a[j - 1] > a[j]) {
                    int temp;
                    temp = a[j - 1];
                    a[j - 1] = a[j];
                    a[j] = temp;
                }
            }
        }
    }

